// ============================================================================
// helper function to categorize a sound label
// takes a raw label from the teachable machine model and returns
// the appropriate category (defaults to 'background' if unknown)
// ============================================================================
export function categorizeSound(label: string): SoundCategory {
  // normalize the label to lowercase and trim whitespace
  // this handles variations like "Fire Alarm" vs "fire alarm"
  const normalizedLabel = label.toLowerCase().trim();
  
  // look up in our mapping, default to 'background' if not found
  // unknown sounds are treated as background noise for safety
  return soundCategoryMap[normalizedLabel] || 'background';
}
